def get_code_depth(code: str) -> int:
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –≥–ª—É–±–∏–Ω—É (—É—Ä–æ–≤–µ–Ω—å) –∫–æ–¥–∞ –≤ –∏–µ—Ä–∞—Ä—Ö–∏–∏ (1.1 -> 2, 1.1.1 -> 3)."""
    if not code or code == "Year": return 0
    return code.count(".") + 1

def get_parent_code(code: str) -> Optional[str]:
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Ä–æ–¥–∏—Ç–µ–ª—å—Å–∫–∏–π –∫–æ–¥."""
    if "." not in code: return None
    return code.rsplit(".", 1)[0]

def get_ancestor_codes(code: str) -> List[str]:
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö –ø—Ä–µ–¥–∫–æ–≤ –∫–æ–¥–∞."""
    ancestors = []
    current = code
    while current:
        ancestors.insert(0, current)
        current = get_parent_code(current)
    return ancestors

def is_descendant_of(code: str, ancestor: str) -> bool:
    """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ code –ø–æ—Ç–æ–º–∫–æ–º ancestor."""
    if code == ancestor: return True
    return code.startswith(ancestor + ".")

def filter_columns_by_nodes(columns: List[str], selected_nodes: Optional[List[str]] = None) -> List[str]:
    """–§–∏–ª—å—Ç—Ä—É–µ—Ç –∫–æ–ª–æ–Ω–∫–∏ –¥–∞—Ç–∞—Å–µ—Ç–∞ –ø–æ –≤—ã–±—Ä–∞–Ω–Ω—ã–º —É–∑–ª–∞–º –∫–ª–∞—Å—Å–∏—Ñ–∏–∫–∞—Ç–æ—Ä–∞."""
    if not selected_nodes:
        return columns
    
    filtered = []
    # –ï—Å–ª–∏ –≤—ã–±—Ä–∞–Ω "Year", –¥–æ–±–∞–≤–ª—è–µ–º –µ–≥–æ, –µ—Å–ª–∏ –æ–Ω –µ—Å—Ç—å –≤ –∫–æ–ª–æ–Ω–∫–∞—Ö (–∫–∞–∫ Year_num)
    if "Year" in selected_nodes and "Year_num" in columns:
        filtered.append("Year_num")

    for col in columns:
        if col == "Year_num": continue
        for node in selected_nodes:
            if node != "Year" and is_descendant_of(col, node):
                filtered.append(col)
                break
    return filtered

