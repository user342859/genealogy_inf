def to_short_name(full_name: str) -> str:
    """'–ò–≤–∞–Ω–æ–≤ –ò–≤–∞–Ω –ò–≤–∞–Ω–æ–≤–∏—á' -> '–ò–≤–∞–Ω–æ–≤ –ò.–ò.'"""
    parts = full_name.strip().replace('.', ' ').split()
    if not parts: return ""
    surname = parts[0]
    initials = ""
    if len(parts) > 1: initials += parts[1][0] + "."
    if len(parts) > 2: initials += parts[2][0] + "."
    return f"{surname} {initials}"

def normalize_authors_set(authors_str: str) -> Set[str]:
    """'–ò–≤–∞–Ω–æ–≤ –ò.–ò.; –ü–µ—Ç—Ä–æ–≤ –ü.–ü.' -> {'–∏–≤–∞–Ω–æ–≤ –∏.–∏.', '–ø–µ—Ç—Ä–æ–≤ –ø.–ø.'}"""
    if not isinstance(authors_str, str): return set()
    raw_names = re.split(r'[;]', authors_str)
    res = set()
    for n in raw_names:
        clean = n.strip()
        if clean:
            clean = re.sub(r'\s+', ' ', clean)
            res.add(clean.lower())
    return res

