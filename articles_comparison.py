def prepare_articles_dataset(
    roots: List[str],
    df_lineage: pd.DataFrame,
    idx_lineage: Dict[str, Set[int]],
    lineage_func: Callable,
    df_articles: pd.DataFrame,
    scope: str = "direct",
    selected_features_keys: Optional[List[str]] = None
) -> Tuple[pd.DataFrame, List[str]]:
    """
    –§–æ—Ä–º–∏—Ä—É–µ—Ç –¥–∞—Ç–∞—Å–µ—Ç –¥–ª—è –∞–Ω–∞–ª–∏–∑–∞.
    1. –ù–∞—Ö–æ–¥–∏—Ç —É—á–µ–Ω–∏–∫–æ–≤ —à–∫–æ–ª.
    2. –§–∏–ª—å—Ç—Ä—É–µ—Ç —Å—Ç–∞—Ç—å–∏.
    3. –í—ã–±–∏—Ä–∞–µ—Ç –Ω—É–∂–Ω—ã–µ –∫–æ–ª–æ–Ω–∫–∏ (—Å —É—á–µ—Ç–æ–º "Year").
    """
    combined_rows = []
    
    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –¥–æ—Å—Ç—É–ø–Ω—ã–µ —á–∏—Å–ª–æ–≤—ã–µ –∫–æ–ª–æ–Ω–∫–∏ (–∫–æ–¥—ã –∫–ª–∞—Å—Å–∏—Ñ–∏–∫–∞—Ç–æ—Ä–∞)
    all_cols = df_articles.columns.tolist()
    classifier_cols = [c for c in all_cols if c not in METADATA_COLS and re.match(r'^[\d\.]+$', c)]
    
    # 1. –°–±–æ—Ä —Å—Ç—Ä–æ–∫
    for root in roots:
        G, _ = lineage_func(df_lineage, idx_lineage, root)
        
        if scope == "direct":
            school_members = set(G.successors(root)) if G.has_node(root) else set()
        else:
            school_members = set(G.nodes()) - {root} if G.has_node(root) else set()

        if not school_members: continue
            
        school_members_short = {to_short_name(n).lower() for n in school_members if n}
        
        # –§–∏–ª—å—Ç—Ä —Å—Ç–∞—Ç–µ–π
        mask = df_articles["Authors"].apply(
            lambda x: not normalize_authors_set(x).isdisjoint(school_members_short)
        )
        school_articles = df_articles[mask].copy()
        
        if not school_articles.empty:
            school_articles["school"] = root
            combined_rows.append(school_articles)

    if not combined_rows:
        return pd.DataFrame(), []

    result_df = pd.concat(combined_rows, ignore_index=True)
    
    # 2. –û–±—Ä–∞–±–æ—Ç–∫–∞ –∫–æ–ª–æ–Ω–æ–∫ (Year -> Year_num)
    if "Year" in df_articles.columns:
        result_df["Year_num"] = pd.to_numeric(result_df["Year"], errors='coerce').fillna(0)
    else:
        result_df["Year_num"] = 0

    # 3. –§–∏–ª—å—Ç—Ä–∞—Ü–∏—è –ø—Ä–∏–∑–Ω–∞–∫–æ–≤ –ø–æ –≤—ã–±—Ä–∞–Ω–Ω—ã–º —É–∑–ª–∞–º
    # –°–Ω–∞—á–∞–ª–∞ –±–µ—Ä–µ–º –≤—Å–µ –≤–æ–∑–º–æ–∂–Ω—ã–µ, –ø–æ—Ç–æ–º —Ñ–∏–ª—å—Ç—Ä—É–µ–º
    available_features = classifier_cols + ["Year_num"]
    
    # –ï—Å–ª–∏ selected_features_keys –ø–µ—Ä–µ–¥–∞–Ω (–Ω–∞–ø—Ä–∏–º–µ—Ä, ["1.1", "Year"]), —Ñ–∏–ª—å—Ç—Ä—É–µ–º
    # –ï—Å–ª–∏ None –∏–ª–∏ –ø—É—Å—Ç–æ - –±–µ—Ä–µ–º –≤—Å—ë
    if selected_features_keys:
        # –í–∞–∂–Ω–æ: filter_columns_by_nodes —É–º–µ–µ—Ç —Ä–∞–±–æ—Ç–∞—Ç—å —Å –∏–µ—Ä–∞—Ä—Ö–∏–µ–π
        final_features = filter_columns_by_nodes(available_features, selected_features_keys)
    else:
        final_features = classifier_cols # –ü–æ —É–º–æ–ª—á–∞–Ω–∏—é –±–µ–∑ –≥–æ–¥–∞, —Ç–æ–ª—å–∫–æ —Ç–µ–º–∞—Ç–∏–∫–∞

    # –ß–∏—Å—Ç–∏–º –¥–∞–Ω–Ω—ã–µ
    for col in final_features:
        result_df[col] = pd.to_numeric(result_df[col], errors='coerce').fillna(0)
    
    # –£–¥–∞–ª—è–µ–º —Å—Ç—Ä–æ–∫–∏, –≥–¥–µ –≤—Å–µ –≤—ã–±—Ä–∞–Ω–Ω—ã–µ –ø—Ä–∏–∑–Ω–∞–∫–∏ = 0 (–µ—Å–ª–∏ —ç—Ç–æ –Ω–µ –≥–æ–¥)
    # (–û–ø—Ü–∏–æ–Ω–∞–ª—å–Ω–æ, –Ω–æ –ø–æ–º–æ–≥–∞–µ—Ç —É–±—Ä–∞—Ç—å —à—É–º)
    
    return result_df, final_features

